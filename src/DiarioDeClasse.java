public class DiarioDeClasse {
    private Professor professor;
    private Turma turma;
    private Disciplina disciplina;
    private Sala sala;

    public DiarioDeClasse(Professor professor, Turma turma, Disciplina disciplina, Sala sala) {
        this.professor = professor;
        this.turma = turma;
        this.disciplina = disciplina;
        this.sala = sala;
    }
}
